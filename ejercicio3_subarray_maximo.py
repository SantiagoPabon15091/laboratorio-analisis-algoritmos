print(f"  Fuerza bruta:       suma = {max_subarray_bruteforce(A1)}")
print(f"  Divide y vencerás:  suma = {max_subarray_divide_conquer(A1, 0, len(A1)-1)}")


#Medición de tiempos
def measure_times(sizes, repetitions=50):
    brute_times = []
    divide_times = []

    for n in sizes:
        arr = [random.randint(-100, 100) for _ in range(n)]

        # Fuerza bruta
        total = 0
        for _ in range(repetitions):
            start = time.perf_counter()
            max_subarray_bruteforce(arr)
            total += time.perf_counter() - start
        brute_times.append(total / repetitions)

        # Divide y vencerás
        total = 0
        for _ in range(repetitions):
            start = time.perf_counter()
            max_subarray_divide_conquer(arr, 0, n - 1)
            total += time.perf_counter() - start
        divide_times.append(total / repetitions)

        print(f"n={n:5d} | Bruta: {brute_times[-1]*1000:.4f} ms "
              f"| Divide: {divide_times[-1]*1000:.4f} ms")

    return brute_times, divide_times


sizes = [10, 50, 100, 200, 500, 1000]
brute_times, divide_times = measure_times(sizes)


#Gráfica
sns.set_theme(style="whitegrid")

fig, axes = plt.subplots(1, 2, figsize=(14, 6))

brute_ms  = [t * 1000 for t in brute_times]
divide_ms = [t * 1000 for t in divide_times]

# Gráfica normal
axes[0].plot(sizes, brute_ms, marker='o', linewidth=2, label='Fuerza bruta O(n²)')
axes[0].plot(sizes, divide_ms, marker='s', linewidth=2, label='Divide y vencerás O(n log n)')
